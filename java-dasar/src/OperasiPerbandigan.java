public class OperasiPerbandigan {
    public static void main(String[] args) {
        int a = 100;
        int b = 100;

        System.out.println(a>b); // false
        System.out.println(a<b); // false
        System.out.println(a>=b); // true
        System.out.println(a<=b); // true
        System.out.println(a==b); // true
        System.out.println(a!=b); // false
    }
}
